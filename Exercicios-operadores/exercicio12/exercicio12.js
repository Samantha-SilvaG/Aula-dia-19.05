//12. Dado um círculo com raio r = 7 , calcule sua área e circunferência. Fórmulas:
//Área: A = π * r2
//Circunferência: C = 2 * π * r
//Considere π = 3.14 .

let pi = 3.14
let raio = 7
let area = pi * ( raio **2)
let circunferencia = 2 * pi * raio
console.log(`Area do circulo: ${area}`)
console.log(`circunferencia: ${circunferencia}`)